x = int(float(input('Liczba x=')))
y = int(float(input('Liczba y=')))
print(x, y, sep='\t', end='\n')
print(x, '+', y, '=', x + y)
print(x + y)
print(x * y)
print(x - y)
print(x / y)  # -->float
print(x // y)  # -->int
print(x**y)  # potegowanie
print(x ** (1 / y))  # pierwiastek
print(x % y)  # modulo
